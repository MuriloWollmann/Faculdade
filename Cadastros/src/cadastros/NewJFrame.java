    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String id = jTextField12.getText();
        boolean encontrado = false;

        for (Pessoa p : pessoas) {
            if (Integer.toString(p.getId()).equals(id)) {
                pessoas.remove(p);
                jLabel15.setText("Cadastro removido com sucesso!");
                jTextField12.setText(""); 
                jTextField10.setText("");
                jTextField11.setText("");
                jTextField9.setText("");    
                encontrado = true;
                break;
            }
        }
        
        if (!encontrado) {
            jLabel15.setText("Pessoa com o ID informado não foi encontrada.");
            jTextField12.setText(""); 
            jTextField10.setText("");
            jTextField11.setText("");
            jTextField9.setText("");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String nome = jTextField1.getText();
        boolean encontrado = false;
        
        for (Pessoa p : pessoas) {
            if(nome.equals(p.getNome())){
                jTextArea1.setText(
                "ID: " + p.getId() + "\n" +
                "Nome: " + p.getNome() + "\n" +
                "Idade: " + p.getIdade() + "\n" +
                "Cidade: " + p.getCidade()
                );
                jTextField1.setText("");
                encontrado = true;
                break;
            } 
        }
        
        if (!encontrado) {
            jTextField1.setText("");
            jTextArea1.setText("Pessoa não encontrada.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

