    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id = jTextField8.getText();
        boolean encontrado = false;

        for (Pessoa p : pessoas) {
            if (Integer.toString(p.getId()).equals(id)) {
                p.setNome(jTextField6.getText());
                p.setIdade(jTextField7.getText());
                p.setCidade(jTextField5.getText());
                jLabel14.setText("Cadastro atualizado com sucesso!");
                jTextField8.setText(""); 
                jTextField6.setText("");
                jTextField7.setText("");
                jTextField5.setText(""); 
                encontrado = true;
                break;
            }
        }
        
        if (!encontrado) {
            jLabel14.setText("Pessoa com o ID informado não foi encontrada.");
            jTextField8.setText(""); 
            jTextField6.setText("");
            jTextField7.setText("");
            jTextField5.setText(""); 
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

